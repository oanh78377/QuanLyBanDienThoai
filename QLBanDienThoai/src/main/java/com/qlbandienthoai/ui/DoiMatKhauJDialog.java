    NhanVienDAO dao = new NhanVienDAO();
  void DoiMK() {
    String userName = this.txtuserName.getText();
    String passWord = new String(this.txtPasword.getPassword());
    String newPassWord = new String(this.txtNewPassword.getPassword());
    String xacNhanMK = new String(this.txtComfrimPassword.getPassword());

    if (!newPassWord.equals(xacNhanMK)) {
        JOptionPane.showMessageDialog(this, "Xác nhận mật khẩu không khớp!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Fetch the current user
    NhanVien nv = dao.selectById(userName);
    if (nv == null || !nv.getMatKhau().equals(passWord)) {
        JOptionPane.showMessageDialog(this, "Mật khẩu hiện tại không đúng!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Update the password
    nv.setMatKhau(newPassWord);
    dao.update(nv);
    JOptionPane.showMessageDialog(this, "Đổi mật khẩu thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
    this.dispose();
}
