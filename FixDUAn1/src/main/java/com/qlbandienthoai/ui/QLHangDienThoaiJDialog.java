 HangDienThoaiDAO dao = new HangDienThoaiDAO();
    int row = -1;
    void init(){
        setLocationRelativeTo(null); // đưa cửa sổ ra giữa màn hình
        this.fillTable(); // đổ dữ liệu nhân viên vào bảng
        this.updateStatus(); // cập nhật trạng thái form
    }
    void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblHangDienThoai.getModel();
        model.setRowCount(0);
        try {
            List<HangDienThoai> list = dao.selectAll();
            for (HangDienThoai hdt : list) {
                Object[] row = {
                    hdt.getMaHangDienThoai(),
                    hdt.getTenHangDienThoai(),
                   
                };
                model.addRow(row);
            }
        } 
        catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }
    void insert(){
       HangDienThoai model = getForm();
        try {
            dao.insert(model);
            this.fillTable();
            this.clearForm();
            MsgBox.alert(this, "Thêm mới thành công!");
        } 
        catch (Exception e) {
            MsgBox.alert(this, "Thêm mới thất bại!");
        }
    }
    void update(){
         HangDienThoai model = getForm();
        try {
            dao.update(model);
            this.fillTable();
            MsgBox.alert(this, "Cập nhật thành công!");
        } 
        catch (Exception e) {
            MsgBox.alert(this, "Cập nhật thất bại!");
        }
    }
    void delete(){
        if(!Auth.isManager()){
            MsgBox.alert(this, "Bạn không có quyền xóa hãng điện thoại!");
        }
        else{
            if(MsgBox.confirm(this, "Bạn có muốn xóa hay không?")){
                String mahdt = txtMaHangDienThoai.getText();
                try {
                    dao.delete(mahdt);
                    this.fillTable();
                    this.clearForm();
                    MsgBox.alert(this, "Xóa thành công!");
                } 
                catch (Exception e) {
                    MsgBox.alert(this, "Xóa thất bại!");
                }
            }
        }
    }
    private void clearForm() {
        HangDienThoai nv = new HangDienThoai();
        this.setForm(nv);
        this.row = -1;
        this.updateStatus();
    }

 

    void first() {
        this.row = 0;
//        this.tblHangDienThoai.setRowSelectionInterval(this.row, this.row);
//        this.setForm(hdt);
        this.edit();
    }

    void prev() {
        if(this.row > 0){
            this.row--;
            this.edit();
        }
    }

    void next() {
        if(this.row < tblHangDienThoai.getRowCount() - 1){
            this.row++;
            this.edit();
        }
    }

    void last() {
        this.row = tblHangDienThoai.getRowCount() - 1;
        this.edit();
    }

    private void edit() {
        String mahdt = (String) tblHangDienThoai.getValueAt(this.row, 0);
        HangDienThoai nv = dao.selectById(mahdt);
        this.setForm(nv);
//        tabs.setSelectedIndex(0);
        this.updateStatus();
    }

    private void updateStatus() {
         boolean edit = (this.row >= 0);
        boolean first = (this.row == 0);
        boolean last = (this.row == tblHangDienThoai.getRowCount() - 1);
        // Trạng thái form
        txtMaHangDienThoai.setEditable(!edit);
        btnThem.setEnabled(!edit);
        btnSua.setEnabled(edit);
        btnXoa.setEnabled(edit);
        
        // Trạng thái điều hướng
        btnFirst.setEnabled(edit && !first);
        btnPrev.setEnabled(edit && !first);
        btnNext.setEnabled(edit && !last);
        btnLast.setEnabled(edit && !last);
    }

    private HangDienThoai getForm() {
        HangDienThoai hdt = new HangDienThoai();
        hdt.setMaHangDienThoai(txtMaHangDienThoai.getText());
        hdt.setTenHangDienThoai(txtTenHangDienThoai.getText());
        return hdt;
    }

    private void setForm(HangDienThoai hdt) {
        txtMaHangDienThoai.setText(hdt.getMaHangDienThoai());
        txtTenHangDienThoai.setText(hdt.getTenHangDienThoai());
        
    }


